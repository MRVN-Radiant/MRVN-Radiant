/*
   WriteR2BSPFile()
   writes a apex bsp file
*/
void WriteR5BSPFile(const char *filename) {
    rbspHeader_t header{};

    /* set up header */
    memcpy(header.ident, g_game->bspIdent, 4);
    header.version = LittleLong(g_game->bspVersion);
    header.mapVersion = 30;
    header.maxLump = 127;

    /* write initial header */
    FILE* file = SafeOpenWrite(filename);
    SafeWrite(file, &header, sizeof(header));    /* overwritten later */


    /* :) */
    {
        char message[64] = "Built with love using MRVN-radiant :)";
        SafeWrite(file, &message, sizeof(message));
    }
    {
        char message[64];
        strncpy(message, StringOutputStream(64)("Version:        ", Q3MAP_VERSION).c_str(), 64);
        SafeWrite(file, &message, sizeof(message));
    }
    {
        time_t t;
        time(&t);
        char message[64];
        strncpy(message, StringOutputStream(64)("Time:           ", asctime(localtime(&t))).c_str(), 64);
        SafeWrite(file, &message, sizeof(message));
    }

    /* Write lumps */
    AddLump(file, header.lumps[R5_LUMP_ENTITIES],                 Titanfall::Bsp::entities);
    AddLump(file, header.lumps[R5_LUMP_TEXTURE_DATA],             ApexLegends::Bsp::textureData);
    AddLump(file, header.lumps[R5_LUMP_VERTICES],                 Titanfall::Bsp::vertices);
    AddLump(file, header.lumps[R5_LUMP_LIGHTPROBE_PARENT_INFOS],  ApexLegends::Bsp::lightprobeParentInfos_stub);  // stub
    AddLump(file, header.lumps[R5_LUMP_SHADOW_ENVIRONMENTS],      ApexLegends::Bsp::shadowEnvironments_stub);     // stub
    AddLump(file, header.lumps[R5_LUMP_MODELS],                   ApexLegends::Bsp::models);
    AddLump(file, header.lumps[R5_LUMP_SURFACE_NAMES],            Titanfall::Bsp::textureDataData);
    AddLump(file, header.lumps[R5_LUMP_CONTENTS_MASKS],           ApexLegends::Bsp::contentsMasks_stub);          // stub
    AddLump(file, header.lumps[R5_LUMP_SURFACE_PROPERTIES],       ApexLegends::Bsp::surfaceProperties_stub);      // stub
    AddLump(file, header.lumps[R5_LUMP_BVH_NODES],                ApexLegends::Bsp::bvhNodes_stub);               // stub
    AddLump(file, header.lumps[R5_LUMP_BVH_LEAF_DATA],            ApexLegends::Bsp::bvhNodeLeafData_stub);        // stub
    AddLump(file, header.lumps[R5_LUMP_ENTITY_PARTITIONS],        Titanfall::Bsp::entityPartitions);
    AddLump(file, header.lumps[R5_LUMP_VERTEX_NORMALS],           Titanfall::Bsp::vertexNormals);

    // GameLump Stub
    {
        header.lumps[R5_LUMP_GAME_LUMP].offset = ftell(file);
        header.lumps[R5_LUMP_GAME_LUMP].length = 40;

        ApexLegends::GameLump_Stub_t gameLump_stub;
        SafeWrite(file, &gameLump_stub.version, 4);
        memcpy(gameLump_stub.magic, "prps", 4);
        SafeWrite(file, &gameLump_stub.magic, 4);
        SafeWrite(file, &gameLump_stub.const0, 4);
        gameLump_stub.offset = LittleLong(ftell(file) + 8);
        SafeWrite(file, &gameLump_stub.offset, 4);
        SafeWrite(file, &gameLump_stub.length, 4);
        SafeWrite(file, &gameLump_stub.zeros, 20);
    }

    AddLump(file, header.lumps[R5_LUMP_UNKNOWN_37],              ApexLegends::Bsp::unknown25_stub);            // stub
    AddLump(file, header.lumps[R5_LUMP_UNKNOWN_38],              ApexLegends::Bsp::unknown26_stub);            // stub
    AddLump(file, header.lumps[R5_LUMP_UNKNOWN_39],              ApexLegends::Bsp::unknown27_stub);            // stub
    AddLump(file, header.lumps[R5_LUMP_CUBEMAPS],                ApexLegends::Bsp::cubemaps_stub);             // stub
    AddLump(file, header.lumps[R5_LUMP_WORLD_LIGHTS],            ApexLegends::Bsp::worldLights_stub);          // stub
    AddLump(file, header.lumps[R5_LUMP_VERTEX_UNLIT],            ApexLegends::Bsp::vertexUnlitVertices);
    AddLump(file, header.lumps[R5_LUMP_VERTEX_LIT_FLAT],         ApexLegends::Bsp::vertexLitFlatVertices);
    AddLump(file, header.lumps[R5_LUMP_VERTEX_LIT_BUMP],         ApexLegends::Bsp::vertexLitBumpVertices);
    AddLump(file, header.lumps[R5_LUMP_VERTEX_UNLIT_TS],         ApexLegends::Bsp::vertexUnlitTSVertices);
    AddLump(file, header.lumps[R5_LUMP_MESH_INDICES],            Titanfall::Bsp::meshIndices);
    AddLump(file, header.lumps[R5_LUMP_MESHES],                  ApexLegends::Bsp::meshes);
    AddLump(file, header.lumps[R5_LUMP_MESH_BOUNDS],             Titanfall::Bsp::meshBounds);
    AddLump(file, header.lumps[R5_LUMP_MATERIAL_SORT],           ApexLegends::Bsp::materialSorts);
    AddLump(file, header.lumps[R5_LUMP_LIGHTMAP_HEADERS],        ApexLegends::Bsp::lightmapHeaders_stub);      // stub
    AddLump(file, header.lumps[R5_LUMP_TWEAK_LIGHTS],            ApexLegends::Bsp::tweakLights_stub);          // stub
    AddLump(file, header.lumps[R5_LUMP_LIGHTMAP_DATA_SKY],       ApexLegends::Bsp::lightmapDataSky_stub);      // stub
    AddLump(file, header.lumps[R5_LUMP_CSM_AABB_NODES],          ApexLegends::Bsp::csmAABBNodes_stub);         // stub
    AddLump(file, header.lumps[R5_LUMP_CELL_BSP_NODES],          Titanfall::Bsp::cellBSPNodes_stub);           // stub
    AddLump(file, header.lumps[R5_LUMP_CELLS],                   Titanfall::Bsp::cells_stub);                  // stub
    AddLump(file, header.lumps[R5_LUMP_OCCLUSION_MESH_VERTICES], Titanfall::Bsp::occlusionMeshVertices);
    AddLump(file, header.lumps[R5_LUMP_OCCLUSION_MESH_INDICES],  Titanfall::Bsp::occlusionMeshIndices);
    AddLump(file, header.lumps[R5_LUMP_CELL_AABB_NODES],         ApexLegends::Bsp::cellAABBNodes);
    AddLump(file, header.lumps[R5_LUMP_OBJ_REFERENCES],          ApexLegends::Bsp::objReferences);
    AddLump(file, header.lumps[R5_LUMP_OBJ_REFERENCE_BOUNDS],    Titanfall::Bsp::objReferenceBounds);
    AddLump(file, header.lumps[R5_LUMP_LIGHTMAP_DATA_RTL_PAGE],  ApexLegends::Bsp::lightmapDataRTLPage_stub);  // stub
    AddLump(file, header.lumps[R5_LUMP_LEVEL_INFO],              ApexLegends::Bsp::levelInfo);

    /* emit bsp size */
    const int size = ftell(file);
    Sys_Printf("Wrote %.1f MB (%d bytes)\n", (float)size / (1024 * 1024), size);

    /* write the completed header */
    fseek(file, 0, SEEK_SET);
    SafeWrite(file, &header, sizeof(header));

    /* close the file */
    fclose(file);
}


/*
   CompileR5BSPFile()
   Compiles a v47 bsp file
*/
void CompileR5BSPFile() {
    for (entity_t &entity : entities) {
        const char *pszClassname = entity.classname();

        #define ENT_IS(classname) striEqual(pszClassname, classname)

        /* visible geo */
        if (ENT_IS("worldspawn")) {
            ApexLegends::BeginModel(entity);

            /* generate bsp meshes from map brushes */
            Shared::MakeMeshes(entity);
            ApexLegends::EmitMeshes(entity);

            ApexLegends::EndModel();
        } else if (ENT_IS("func_occluder")) {
            Titanfall::EmitOcclusionMeshes( entity );
            continue; // Don't emit as entity
        } else {
        }

        ApexLegends::EmitEntity(entity);

        #undef ENT_IS
    }

    Shared::MakeVisReferences();
    Shared::visRoot = Shared::MakeVisTree(Shared::visRefs, 1e30f);
    Shared::MergeVisTree(Shared::visRoot);
    ApexLegends::EmitVisTree();

    Titanfall::EmitEntityPartitions();

    ApexLegends::EmitLevelInfo();

    Titanfall::EmitStubs();
    ApexLegends::EmitStubs();
}


/*
    EmitObjReferences
    emits obj references to the bsp
*/
std::size_t ApexLegends::EmitObjReferences(Shared::visNode_t &node) {
    for (Shared::visRef_t &ref : node.refs) {
        Titanfall::ObjReferenceBounds_t &rb = Titanfall::Bsp::objReferenceBounds.emplace_back();
        rb.maxs = ref.minmax.maxs;
        rb.mins = ref.minmax.mins;

        ApexLegends::Bsp::objReferences.emplace_back(ref.index);
    }

    return ApexLegends::Bsp::objReferences.size() - node.refs.size();
}


int ApexLegends::EmitVisChildrenOfTreeNode(Shared::visNode_t node) {
    int index = ApexLegends::Bsp::cellAABBNodes.size();  // Index of first child of node

    for (std::size_t i = 0; i < node.children.size(); i++) {
        Shared::visNode_t &n = node.children.at(i);

        ApexLegends::CellAABBNode_t &bn = ApexLegends::Bsp::cellAABBNodes.emplace_back();
        bn.maxs = n.minmax.maxs;
        bn.mins = n.minmax.mins;
        bn.childCount = n.children.size();
        bn.childFlags = 0x40;

        if (n.refs.size()) {
            bn.objRefOffset = ApexLegends::EmitObjReferences(n);
            bn.objRefCount = n.refs.size();
            bn.objRefFlags = 0x40;
        }
    }

    for (std::size_t i = 0; i < node.children.size(); i++) {
        int firstChild = ApexLegends::EmitVisChildrenOfTreeNode(node.children.at(i));

        ApexLegends::CellAABBNode_t &bn = ApexLegends::Bsp::cellAABBNodes.at(index + i);
        bn.firstChild = firstChild;
    }

    return index;
}

/*
    BeginModel
*/
void ApexLegends::BeginModel(entity_t &entity) {
    Sys_FPrintf( SYS_VRB, "   BeginModel: \"%s\"\n", entity.classname() );
    ApexLegends::Model_t &model = ApexLegends::Bsp::models.emplace_back();
    model.meshIndex = ApexLegends::Bsp::meshes.size();
    model.bvhNodeIndex = 0;
    model.bvhLeafIndex = 0;
    model.vertexIndex = Titanfall::Bsp::vertices.size();
    model.vertexFlags = 0;
}

/*
    EndModel
*/
void ApexLegends::EndModel() {
    ApexLegends::Model_t &model = ApexLegends::Bsp::models.back();
    model.meshCount = ApexLegends::Bsp::meshes.size() - model.meshIndex;

    for( Titanfall::MeshBounds_t &meshBounds : Titanfall::Bsp::meshBounds ) {
        model.minmax.extend(meshBounds.origin - meshBounds.extents);
        model.minmax.extend(meshBounds.origin + meshBounds.extents);
    }

    Sys_FPrintf( SYS_VRB, "       numMeshes: %i\n", model.meshCount );
    Sys_FPrintf( SYS_VRB, "   EndModel\n" );
}


/*
    EmitVisTree
    Emits the vistree to the bsp file
*/
void ApexLegends::EmitVisTree() {
    Shared::visNode_t &root = Shared::visRoot;

    ApexLegends::CellAABBNode_t &bn = ApexLegends::Bsp::cellAABBNodes.emplace_back();
    bn.maxs = root.minmax.maxs;
    bn.mins = root.minmax.mins;
    bn.firstChild = ApexLegends::Bsp::cellAABBNodes.size();
    bn.childCount = root.children.size();
    bn.childFlags = 0x40;

    if (root.refs.size()) {
        bn.objRefOffset = EmitObjReferences(root);
        bn.objRefCount = root.refs.size();
        bn.objRefFlags = 0x40;
    }

    EmitVisChildrenOfTreeNode(Shared::visRoot);
}


/*
    EmitMeshes()
    writes the mesh list to the bsp
*/
void ApexLegends::EmitMeshes(const entity_t &e) {
    for (const Shared::Mesh_t &mesh : Shared::meshes) {
        ApexLegends::Mesh_t &m = ApexLegends::Bsp::meshes.emplace_back();
        m.flags = mesh.shaderInfo->surfaceFlags;
        m.triOffset = Titanfall::Bsp::meshIndices.size();
        m.triCount = mesh.triangles.size() / 3;

        int vertexOffset;
        if (CHECK_FLAG(mesh.shaderInfo->surfaceFlags, S_VERTEX_LIT_BUMP)) {
            vertexOffset = ApexLegends::Bsp::vertexLitBumpVertices.size();
        } else if (CHECK_FLAG(mesh.shaderInfo->surfaceFlags, S_VERTEX_UNLIT)) {
            vertexOffset = ApexLegends::Bsp::vertexUnlitVertices.size();
        } else if (CHECK_FLAG(mesh.shaderInfo->surfaceFlags, S_VERTEX_UNLIT_TS)) {
            vertexOffset = ApexLegends::Bsp::vertexUnlitVertices.size();
        } else {
            vertexOffset = ApexLegends::Bsp::vertexLitFlatVertices.size();
        }


        // Emit textrue related structs
        uint32_t textureIndex = ApexLegends::EmitTextureData(*mesh.shaderInfo);
        m.materialOffset = ApexLegends::EmitMaterialSort(textureIndex);
        MinMax  aabb;

        // Save vertices and vertexnormals
        for (std::size_t i = 0; i < mesh.vertices.size(); i++) {
            Shared::Vertex_t vertex = mesh.vertices.at(i);

            // Check against aabb
            aabb.extend(vertex.xyz);

            if (CHECK_FLAG(mesh.shaderInfo->surfaceFlags, S_VERTEX_LIT_BUMP)) {
                ApexLegends::EmitVertexLitBump(vertex);
            } else if (CHECK_FLAG(mesh.shaderInfo->surfaceFlags, S_VERTEX_UNLIT)) {
                ApexLegends::EmitVertexUnlit(vertex);
            } else if (CHECK_FLAG(mesh.shaderInfo->surfaceFlags, S_VERTEX_UNLIT_TS)) {
                ApexLegends::EmitVertexUnlitTS(vertex);
            } else {
                ApexLegends::EmitVertexLitFlat(vertex);
            }
        }

        // Save triangles
        for (uint16_t triangle : mesh.triangles) {
            Titanfall::Bsp::meshIndices.emplace_back(triangle + vertexOffset);
        }

        // Save MeshBounds
        Titanfall::MeshBounds_t &mb = Titanfall::Bsp::meshBounds.emplace_back();
        mb.origin = (aabb.maxs + aabb.mins) / 2;
        mb.extents = (aabb.maxs - aabb.mins) / 2;
    }
}


/*
    EmitTextureData()
    Emits texture data and returns its index
*/
uint32_t ApexLegends::EmitTextureData(shaderInfo_t shader) {
    std::string  tex = shader.shader.c_str();
    std::size_t  index;

    // Strip 'textures/'
    tex.erase(tex.begin(), tex.begin() + strlen("textures/"));
    std::replace(tex.begin(), tex.end(), '/', '\\');  // Do we even need to do this?

    // Check if it's already saved
    std::string table = std::string(Titanfall::Bsp::textureDataData.begin(), Titanfall::Bsp::textureDataData.end());
    index = table.find(tex);
    if (index != std::string::npos) {
        // Is already saved, find the index of its textureData
        for (std::size_t i = 0; i < ApexLegends::Bsp::textureData.size(); i++) {
            ApexLegends::TextureData_t &td = ApexLegends::Bsp::textureData.at(i);

            if (td.surfaceIndex == index) {
                return i;
            }
        }
    }

    // Wasn't already saved, save it
    index = ApexLegends::Bsp::textureData.size();

    // Add to Table
    StringOutputStream data;
    data << tex.c_str();
    std::vector<char> str = { data.begin(), data.end() + 1 };

    ApexLegends::TextureData_t &td = ApexLegends::Bsp::textureData.emplace_back();
    td.surfaceIndex = Titanfall::Bsp::textureDataData.size();
    td.sizeX = shader.shaderImage->width;
    td.sizeY = shader.shaderImage->height;
    td.flags = shader.surfaceFlags;

    Titanfall::Bsp::textureDataData.insert(Titanfall::Bsp::textureDataData.end(), str.begin(), str.end());

    return index;
}


/*
    EmitMaterialSort()
    Tries to create a material sort of the last texture
*/
uint16_t ApexLegends::EmitMaterialSort(uint32_t index) {
        /* Check if the material sort we need already exists */
        std::size_t pos = 0;
        for (ApexLegends::MaterialSort_t &ms : ApexLegends::Bsp::materialSorts) {
            if (ms.textureData == index) {
                return pos;
            }
            pos++;
        }

        ApexLegends::MaterialSort_t &ms = ApexLegends::Bsp::materialSorts.emplace_back();
        ms.textureData = index;

        return pos;
}


